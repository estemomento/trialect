      <p>秦汉到唐宋年间，不少汉人迁入茂名地区居住。由于广州在岭南地区的强大辐射，他们的古汉语基本上和珠三角发展保持同步，但又由于地处偏远，受到的百越语言影响比珠三角更多。在这种合力下，茂名粤语的雏形形成了。</p>
