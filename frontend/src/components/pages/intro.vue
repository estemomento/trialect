      <h2>二、粤语</h2>
      <p>茂名地区的粤语主要有高阳、吴化两片，另外有一些其他片区的小方言岛。</p>
      <h3>1. 高阳片（茂名白话、高州白话、信宜白话）</h3>
      <p>高阳片粤语是茂名地区使用范围最大、人数最多、影响力最大的一类方言，与粤语广州话能互相通话。茂南、高州、信宜的市区以及48个镇（街道）全部或大部分讲高阳片粤语。</p>
      <p>高阳片粤语相对广州话而言，语音上最大的特色是独立的日母/ɳ/（例如「二」）和心母/ɬ/（例如「三」），懒音、吞音、连读等也相对较少，同时也有大量广州话所没有的语音现象如变调指小，相对保留了更多的古粤语元素。</p>
      <h3>2. 吴化片（化州话）</h3>
      <p>吴化片粤语是化州市的主要方言，分布在化州市全部22个镇（街道），根据特点和地理位置被分为两类：以化州市区为代表的上江话和以长歧为代表的下江话。</p>
      <h4>a. 上江话</h4>
      <p>讲上江话的有包括化州市区在内的14个镇（街），上江话可以勉强和广州话互通，但广州人会觉得难懂，与高阳片粤语可以互通。</p>
      <p>上江话大概可再分为两部分：化北和化中。化北的上江话和高阳粤语较接近，仅阳平归入阳上，没有浊塞音，精组读若章组。化中的上江话部分地区精章组区分（精=/teŋ/，蒸=/tʃeŋ/），有浊塞音d，构成t/d对立。</p>
      <h4>b. 下江话</h4>
      <p>讲下江话的有3个镇，未经过一定适应，下江话和广州话、高阳片粤语不能互通。</p>
      <p>下江话有完整的全清-次清-全浊塞音对立，词汇亦极富特色，有专家称其为粤语的“活化石”。</p>
      <h3>3. 其他</h3>
      <p>以下简介一些区内小众的粤语方言。</p>
      <h4>a. 马兰话</h4>
      <p>马兰话是以四邑片粤语为基础，混合了高阳片粤语和闽语元素的一种特别的粤语系方言，分布在电白区大衙的几个山村里，目前处于濒危状态。</p>
      <h4>b. 旧时正话</h4>
      <p>旧时正话是以明代官话为基础，吸收粤语、闽语元素形成的一种军话类方言，目前归属未定，姑且按语音特点归入粤语中介绍。旧时正话是旧时神电卫的江浙驻军及后代发展出来的，后来电城被多次劫掠，操旧时正话的人们也流散到周边的山村。目前同马兰话一样处于濒危状态。</p>
      <h4>c. 容县话</h4>
      <p>容县话属于勾漏片粤语，分布在信宜金垌镇西北部的几个村庄。</p>
      <h4>d. 罗定话</h4>
      <p>罗定话属于广府片粤语，分布在信宜思贺镇。</p>
